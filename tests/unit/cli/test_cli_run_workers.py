"""Phase 10 — ``openpathai run`` gained --workers / --parallel-mode / --snakefile."""

from __future__ import annotations

from pathlib import Path

import pytest
from typer.testing import CliRunner

from openpathai.cli.main import app

runner = CliRunner()


def _pipeline() -> str:
    return str(Path(__file__).resolve().parents[3] / "pipelines" / "supervised_synthetic.yaml")


def test_run_with_threaded_workers(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("OPENPATHAI_HOME", str(tmp_path))
    result = runner.invoke(
        app,
        [
            "run",
            _pipeline(),
            "--cache-root",
            str(tmp_path / "cache"),
            "--output-dir",
            str(tmp_path / "run"),
            "--workers",
            "4",
            "--parallel-mode",
            "thread",
            "--no-audit",
        ],
    )
    assert result.exit_code == 0, result.stdout
    assert "parallel=thread workers=4" in result.stdout


