def test_run_snakefile_exports_without_running(tmp_path: Path) -> None:
    out = tmp_path / "demo.smk"
    result = runner.invoke(
        app,
        [
            "run",
            _pipeline(),
            "--snakefile",
            str(out),
            "--no-audit",
        ],
    )
    assert result.exit_code == 0, result.stdout
    assert out.is_file()
    body = out.read_text(encoding="utf-8")
    assert body.startswith("# Generated by openpathai.pipeline.snakemake")
    assert "rule all:" in body
    # --snakefile is export-only: no manifest written under --output-dir.
    assert "manifest:" not in result.stdout


